# simple while loop
# initialize loop−control variable
x = 0

# test loop−control variable at beginning of loop
while x < 10:
   # print the value of x  each time through the while loop
   # increment loop−control variable
   print(x, end=' ')
   x += 1        

print()
# x has bigger value printed than value in loop
print("Final value of x is:", x)
